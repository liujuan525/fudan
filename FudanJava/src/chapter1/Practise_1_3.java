public class Practise_1_3 {
    public static void main(String[] args) {
        double radius = 5.5;
        double area;
        double len;
        area = Math.PI * radius * radius;
        len = Math.PI * 2 * radius;
        System.out.println("圆面积：" + area);
        System.out.println("圆周长：" + len);
    }
}
